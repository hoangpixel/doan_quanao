    public void headerTable()
    {
        Vector header = new Vector();
        header.add("Mã CTKMSP");
        header.add("Mã CTKM");
        header.add("Mã SP");
        header.add("PTGG");
        model = new DefaultTableModel(header,0)
                {
                    @Override
                    public boolean isCellEditable(int row,int column)
                {
                    return false;
                }
                };
              
        tbCTKMSP.setModel(model);
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(JLabel.CENTER);

        // Căn nội dung ra chính giữa
        for (int i = 0; i < tbCTKMSP.getColumnCount(); i++) {
        tbCTKMSP.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
}

        //căn header ra center
        JTableHeader headerTB = tbCTKMSP.getTableHeader();
        DefaultTableCellRenderer center = (DefaultTableCellRenderer) headerTB.getDefaultRenderer();
        center.setHorizontalAlignment(JLabel.CENTER);
        headerTB.setFont(new Font("Segoe UI",Font.BOLD,14));
        
        tbCTKMSP.setRowHeight(30);
        tbCTKMSP.setFocusable(false);
        tbCTKMSP.setAutoCreateRowSorter(true);
        tbCTKMSP.setDefaultEditor(Object.class, null);
        tbCTKMSP.setShowVerticalLines(false);        
    }
    
    public void updateTB(ArrayList<ChuongTrinhKhuyenMaiSanPhamDTO> dskq)
    {
        model.setRowCount(0);
        for(ChuongTrinhKhuyenMaiSanPhamDTO ct : dskq)
        {
            Vector row = new Vector();
            row.add(ct.getMactkmsp());
            row.add(ct.getMactkm());
            row.add(ct.getMasp());
            row.add(ct.getPtgg());
            model.addRow(row);
        }
        tbCTKMSP.setModel(model);
    }
    public void dsCTKMSP() 
    {
        ChuongTrinhKhuyenMaiSanPhamBUS bus = new ChuongTrinhKhuyenMaiSanPhamBUS();
        if(ChuongTrinhKhuyenMaiSanPhamBUS.ds == null)
        {
            bus.docDSCTKMSP();
        }
        kq = bus.layDsTheoMaCTKM(mactkm);
        updateTB(kq);
    }
