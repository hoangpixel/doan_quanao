    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String tim = txtSearch.getText().trim();
        int i = jComboBox1.getSelectedIndex();
        ArrayList<ChuongTrinhKhuyenMaiSanPhamDTO> dskq = new ArrayList<>();
        for(ChuongTrinhKhuyenMaiSanPhamDTO ct : kq)
        {
            switch(i)
            {
                case 0 : 
                {
                    if(tim.contains(String.valueOf(ct.getMactkmsp())))
                    {
                        dskq.add(ct);
                    }
                    break;
                }
                case 1:
                {
                    if(tim.contains(String.valueOf(ct.getMasp())))
                    {
                        dskq.add(ct);
                    }
                    break;
                }
                case 2:
                {
                    if(tim.contains(String.valueOf(ct.getPtgg())))
                    {
                        dskq.add(ct);
                    }
                    break;
                }
            }
        }
        updateTB(dskq);
        
        if(dskq.isEmpty())
        {
            String mess = "Không tìm thấy kết quả: " + tim + " trong dữ liệu";
            JLabel lbNull = new JLabel(mess);
            lbNull.setFont(new Font("Segoe UI",Font.BOLD,16));
            JOptionPane.showMessageDialog(this, lbNull,"Thông báo",JOptionPane.ERROR_MESSAGE);              
        }
    }//GEN-LAST:event_btnSearchActionPerformed


    
