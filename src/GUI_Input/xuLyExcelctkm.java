    private void btnGhiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGhiActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Chọn nơi lưu file Excel");
        fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        fileChooser.setSelectedFile(new File("DanhSachCTKM.xlsx"));

        int userSelection = fileChooser.showSaveDialog(this);

        if (userSelection == JFileChooser.APPROVE_OPTION) {
            File fileToSave = fileChooser.getSelectedFile();
            String filePath = fileToSave.getAbsolutePath();

            if (!filePath.endsWith(".xlsx")) {
                filePath += ".xlsx";
            }

            try (Workbook workbook = new XSSFWorkbook()) {
                Sheet sheet = workbook.createSheet("Danh sách CTKM");

                CellStyle headerStyle = workbook.createCellStyle();
                headerStyle.setAlignment(HorizontalAlignment.CENTER);
                headerStyle.setFillForegroundColor(IndexedColors.LIGHT_BLUE.getIndex());
                headerStyle.setFillPattern(FillPatternType.SOLID_FOREGROUND);
                headerStyle.setBorderTop(BorderStyle.THIN);
                headerStyle.setBorderBottom(BorderStyle.THIN);
                headerStyle.setBorderLeft(BorderStyle.THIN);
                headerStyle.setBorderRight(BorderStyle.THIN);

                org.apache.poi.ss.usermodel.Font headerFont = workbook.createFont();
                headerFont.setBold(true);
                headerFont.setFontHeightInPoints((short) 12);
                headerStyle.setFont(headerFont);

                CellStyle dataStyle = workbook.createCellStyle();
                dataStyle.setAlignment(HorizontalAlignment.CENTER);
                dataStyle.setBorderTop(BorderStyle.THIN);
                dataStyle.setBorderBottom(BorderStyle.THIN);
                dataStyle.setBorderLeft(BorderStyle.THIN);
                dataStyle.setBorderRight(BorderStyle.THIN);

                Row headerRow = sheet.createRow(0);
                String[] headers = {"Mã CTKM", "Ngày bắt đầu", "Ngày kết thúc"};
                for (int i = 0; i < headers.length; i++) {
                    Cell cell = headerRow.createCell(i);
                    cell.setCellValue(headers[i]);
                    cell.setCellStyle(headerStyle);
                }

                int rowNum = 1;
                for (ChuongTrinhKhuyenMaiDTO ct : dskq) {
                    Row row = sheet.createRow(rowNum++);
                    row.createCell(0).setCellValue(ct.getMaCTKM());
                    row.createCell(1).setCellValue(ct.getNgayBD());
                    row.createCell(2).setCellValue(ct.getNgayKT());

                    for (int i = 0; i < headers.length; i++) {
                        row.getCell(i).setCellStyle(dataStyle);
                    }
                }

                for (int i = 0; i < headers.length; i++) {
                    sheet.autoSizeColumn(i);
                }

                try (FileOutputStream outputStream = new FileOutputStream(filePath)) {
                    workbook.write(outputStream);
                    JLabel lb = new JLabel("Xuất file Excel thành công!");
                    lb.setFont(new java.awt.Font("Segoe UI", java.awt.Font.BOLD, 16));
                    JOptionPane.showMessageDialog(this, lb, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }

            } catch (IOException e) {
                JLabel lb = new JLabel("Lỗi khi xuất file Excel: " + e.getMessage());
                lb.setFont(new java.awt.Font("Segoe UI", java.awt.Font.BOLD, 16));
                JOptionPane.showMessageDialog(this, lb, "Lỗi", JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnGhiActionPerformed

    
    
    private void btnDocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDocActionPerformed
// Mở hộp thoại chọn file Excel
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Chọn File Excel");
        fileChooser.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter("Excel files", "xlsx"));
        int result = fileChooser.showOpenDialog(this);

        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            ArrayList<ChuongTrinhKhuyenMaiDTO> excelData = readExcelFile(selectedFile);

            // Kiểm tra dữ liệu đọc được từ file
            if (excelData != null && !excelData.isEmpty()) {
                // Làm mới dữ liệu trong ChuongTrinhKhuyenMaiBUS.ds
                ChuongTrinhKhuyenMaiBUS bus = new ChuongTrinhKhuyenMaiBUS();
                ChuongTrinhKhuyenMaiBUS.ds = null; // Xóa danh sách cũ
                bus.docDSCTKM(); // Đọc lại từ cơ sở dữ liệu

                // Kiểm tra và thêm dữ liệu vào cơ sở dữ liệu
                int successCount = 0;
                int skipCount = 0;
                for (ChuongTrinhKhuyenMaiDTO ct : excelData) {
                    // Kiểm tra mã CTKM đã tồn tại chưa
                    if (bus.ktraMa(ct.getMaCTKM())) {
                        skipCount++;
                        continue; // Bỏ qua bản ghi trùng lặp
                    }
                    bus.them(ct); // Thêm vào cơ sở dữ liệu và ChuongTrinhKhuyenMaiBUS.ds
                    successCount++;
                }

                // Làm mới dskq và cập nhật JTable
                dskq.clear();
                dskq.addAll(excelData);
                gui.updateTB(dskq);

                // Hiển thị thông báo
//                String message = "Đọc file Excel thành công!\n" +
//                        "Số bản ghi đọc được: " + excelData.size() + "\n" +
//                        "Số bản ghi thêm vào cơ sở dữ liệu: " + successCount + "\n" +
//                        "Số bản ghi bỏ qua (trùng mã): " + skipCount;
//                JOptionPane.showMessageDialog(this, message, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                            JLabel lb = new JLabel("Đọc dữ liệu thành công!");
            lb.setFont(new Font("Segoe UI", Font.BOLD, 16));
            JOptionPane.showMessageDialog(this, lb, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                // Cập nhật lại giao diện
                gui.docSQL();
            } else {
                JOptionPane.showMessageDialog(this, "Không có dữ liệu trong file Excel", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDocActionPerformed
    private ArrayList<ChuongTrinhKhuyenMaiDTO> readExcelFile(File file) {
        ArrayList<ChuongTrinhKhuyenMaiDTO> dataList = new ArrayList<>();
        try (FileInputStream fis = new FileInputStream(file);
             Workbook workbook = new XSSFWorkbook(fis)) {
            Sheet sheet = workbook.getSheetAt(0); // Đọc từ sheet đầu tiên
            SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy"); // Định dạng mới
            SimpleDateFormat parseFormat = new SimpleDateFormat("yyyy-MM-dd"); // Định dạng để parse nếu cần

            for (Row row : sheet) {
                if (row.getRowNum() == 0) continue; // Bỏ qua hàng tiêu đề

                // Kiểm tra dòng có dữ liệu không
                if (row.getCell(0) == null || row.getCell(1) == null || row.getCell(2) == null) {
                    continue; // Bỏ qua dòng nếu thiếu dữ liệu
                }

                try {
                    // Đọc Mã CTKM
                    Cell maCTKMCell = row.getCell(0);
                    if (maCTKMCell.getCellType() != CellType.NUMERIC) {
                        throw new IllegalStateException("Mã CTKM phải là số nguyên tại dòng " + (row.getRowNum() + 1));
                    }
                    int maCTKM = (int) maCTKMCell.getNumericCellValue();

                    // Đọc Ngày bắt đầu
                    Cell ngayBDCell = row.getCell(1);
                    String ngayBD;
                    if (ngayBDCell.getCellType() == CellType.STRING) {
                        String rawNgayBD = ngayBDCell.getStringCellValue();
                        try {
                            // Thử parse chuỗi ngày để kiểm tra định dạng
                            Date date = dateFormat.parse(rawNgayBD);
                            ngayBD = dateFormat.format(date); // Đảm bảo định dạng dd/MM/yyyy
                        } catch (Exception e) {
                            // Nếu chuỗi không đúng định dạng dd/MM/yyyy, thử parse từ định dạng yyyy-MM-dd
                            try {
                                Date date = parseFormat.parse(rawNgayBD);
                                ngayBD = dateFormat.format(date); // Chuyển sang định dạng dd/MM/yyyy
                            } catch (Exception ex) {
                                throw new IllegalStateException("Ngày bắt đầu không đúng định dạng tại dòng " + (row.getRowNum() + 1));
                            }
                        }
                    } else if (ngayBDCell.getCellType() == CellType.NUMERIC && DateUtil.isCellDateFormatted(ngayBDCell)) {
                        ngayBD = dateFormat.format(ngayBDCell.getDateCellValue()); // Định dạng thành dd/MM/yyyy
                    } else {
                        throw new IllegalStateException("Ngày bắt đầu không đúng định dạng tại dòng " + (row.getRowNum() + 1));
                    }

                    // Đọc Ngày kết thúc
                    Cell ngayKTCell = row.getCell(2);
                    String ngayKT;
                    if (ngayKTCell.getCellType() == CellType.STRING) {
                        String rawNgayKT = ngayKTCell.getStringCellValue();
                        try {
                            // Thử parse chuỗi ngày để kiểm tra định dạng
                            Date date = dateFormat.parse(rawNgayKT);
                            ngayKT = dateFormat.format(date); // Đảm bảo định dạng dd/MM/yyyy
                        } catch (Exception e) {
                            // Nếu chuỗi không đúng định dạng dd/MM/yyyy, thử parse từ định dạng yyyy-MM-dd
                            try {
                                Date date = parseFormat.parse(rawNgayKT);
                                ngayKT = dateFormat.format(date); // Chuyển sang định dạng dd/MM/yyyy
                            } catch (Exception ex) {
                                throw new IllegalStateException("Ngày kết thúc không đúng định dạng tại dòng " + (row.getRowNum() + 1));
                            }
                        }
                    } else if (ngayKTCell.getCellType() == CellType.NUMERIC && DateUtil.isCellDateFormatted(ngayKTCell)) {
                        ngayKT = dateFormat.format(ngayKTCell.getDateCellValue()); // Định dạng thành dd/MM/yyyy
                    } else {
                        throw new IllegalStateException("Ngày kết thúc không đúng định dạng tại dòng " + (row.getRowNum() + 1));
                    }

                    ChuongTrinhKhuyenMaiDTO dto = new ChuongTrinhKhuyenMaiDTO(maCTKM, ngayBD, ngayKT);
                    dataList.add(dto);

                } catch (IllegalStateException e) {
                    JOptionPane.showMessageDialog(this, e.getMessage(), "Lỗi dữ liệu", JOptionPane.ERROR_MESSAGE);
                    continue; // Bỏ qua dòng lỗi
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi đọc file Excel: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
        return dataList;
    }

