    private ArrayList<ChuongTrinhKhuyenMaiSanPhamDTO> readExcelFile(File file) {
        ArrayList<ChuongTrinhKhuyenMaiSanPhamDTO> dataList = new ArrayList<>();
        try (FileInputStream fis = new FileInputStream(file);
             Workbook workbook = new XSSFWorkbook(fis)) {
            Sheet sheet = workbook.getSheetAt(0); // Đọc từ sheet đầu tiên

            for (Row row : sheet) {
                if (row.getRowNum() == 0) continue; // Bỏ qua hàng tiêu đề

                // Kiểm tra dòng có dữ liệu không
                if (row.getCell(0) == null || row.getCell(1) == null || row.getCell(2) == null) {
                    continue; // Bỏ qua dòng nếu thiếu dữ liệu
                }

                try {
                    // Đọc Mã CTKM
                    Cell maCTKMSPCell = row.getCell(0);
                    if (maCTKMSPCell.getCellType() != CellType.NUMERIC) {
                        throw new IllegalStateException("Mã CTKMSP phải là số nguyên tại dòng " + (row.getRowNum() + 1));
                    }
                    int maCTKMSP = (int) maCTKMSPCell.getNumericCellValue();
                    Cell maCTKMCell = row.getCell(1);
                    if(maCTKMCell.getCellType() != CellType.NUMERIC)
                    {
                        throw new IllegalStateException("Mã CTKM phải là số nguyên tại dòng " + (row.getRowNum() + 1));       
                    }
                    int maCTKM = (int) maCTKMCell.getNumericCellValue();
                    Cell maSPcell = row.getCell(2);
                    if(maSPcell.getCellType() != CellType.NUMERIC)
                    {
                        throw new IllegalStateException("Mã SP phải là số nguyên tại dòng " + (row.getRowNum() + 1));       
                    }
                    int maSP = (int) maSPcell.getNumericCellValue();
                    Cell ptggCell = row.getCell(3);
                    if(ptggCell.getCellType() != CellType.NUMERIC)
                    {
                        throw new IllegalStateException("PTGG phải là số nguyên tại dòng " + (row.getRowNum() + 1));       
                    }
                    int ptgg = (int) ptggCell.getNumericCellValue();


                    ChuongTrinhKhuyenMaiSanPhamDTO dto = new ChuongTrinhKhuyenMaiSanPhamDTO(maCTKMSP,maCTKM,maSP,ptgg);
                    dataList.add(dto);

                } catch (IllegalStateException e) {
                    JOptionPane.showMessageDialog(this, e.getMessage(), "Lỗi dữ liệu", JOptionPane.ERROR_MESSAGE);
                    continue; // Bỏ qua dòng lỗi
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi đọc file Excel: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
        return dataList;
    }

