    private void loadCTKMHD(int maCTKMHD) {
        ChuongTrinhKhuyenMaiHoaDonDTO ct = new ChuongTrinhKhuyenMaiHoaDonBUS().layCTKMHDTheoMa(maCTKMHD);
        txtMaCTKM.setText(String.valueOf(ct.getMaCTKM()));
        txtTTHD.setText(String.valueOf(ct.getSoTienHD()));
        txtPTGG.setText(String.valueOf(ct.getPhanTramGiamGia()));
    }
