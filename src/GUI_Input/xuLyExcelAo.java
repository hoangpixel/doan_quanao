    private void btnGhiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGhiActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Chọn nơi lưu file Excel Áo");
        fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        fileChooser.setSelectedFile(new File("DanhSachAo.xlsx")); // Đổi tên file

        int userSelection = fileChooser.showSaveDialog(this);

        if (userSelection == JFileChooser.APPROVE_OPTION) {
            File fileToSave = fileChooser.getSelectedFile();
            String filePath = fileToSave.getAbsolutePath();

            if (!filePath.toLowerCase().endsWith(".xlsx")) {
                filePath += ".xlsx";
            }

            File existingFile = new File(filePath);
            if (existingFile.exists()) {
                int result = JOptionPane.showConfirmDialog(this,
                        "File đã tồn tại, bạn có muốn ghi đè không?",
                        "Xác nhận ghi đè", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
                if (result == JOptionPane.NO_OPTION) return;
            }

            try (Workbook workbook = new XSSFWorkbook()) {
                Sheet sheet = workbook.createSheet("Danh sách Áo"); // Đổi tên sheet

                // --- Cell Styles ---
                CellStyle headerStyle = workbook.createCellStyle(); /* ... (copy style từ ví dụ trước) ... */
                 headerStyle.setAlignment(HorizontalAlignment.CENTER);
                headerStyle.setFillForegroundColor(IndexedColors.LIGHT_BLUE.getIndex());
                headerStyle.setFillPattern(FillPatternType.SOLID_FOREGROUND);
                headerStyle.setBorderTop(BorderStyle.THIN);
                headerStyle.setBorderBottom(BorderStyle.THIN);
                headerStyle.setBorderLeft(BorderStyle.THIN);
                headerStyle.setBorderRight(BorderStyle.THIN);
                 org.apache.poi.ss.usermodel.Font headerFont = workbook.createFont();
                headerFont.setBold(true);
                headerFont.setFontHeightInPoints((short) 12);
                headerStyle.setFont(headerFont);

                CellStyle dataStyle = workbook.createCellStyle(); /* ... */
                 dataStyle.setAlignment(HorizontalAlignment.LEFT);
                dataStyle.setBorderTop(BorderStyle.THIN);
                dataStyle.setBorderBottom(BorderStyle.THIN);
                dataStyle.setBorderLeft(BorderStyle.THIN);
                dataStyle.setBorderRight(BorderStyle.THIN);

                CellStyle numberStyle = workbook.createCellStyle(); /* ... */
                 numberStyle.setAlignment(HorizontalAlignment.RIGHT);
                numberStyle.setBorderTop(BorderStyle.THIN);
                numberStyle.setBorderBottom(BorderStyle.THIN);
                numberStyle.setBorderLeft(BorderStyle.THIN);
                numberStyle.setBorderRight(BorderStyle.THIN);
                // --- End Cell Styles ---

                Row headerRow = sheet.createRow(0);
                String[] headers = {"Mã Loại", "Tên Áo"}; // Sửa headers
                for (int i = 0; i < headers.length; i++) {
                    Cell cell = headerRow.createCell(i);
                    cell.setCellValue(headers[i]);
                    cell.setCellStyle(headerStyle);
                }

                int rowNum = 1;
                AoBUS tempBus = new AoBUS(); // Lấy dữ liệu mới nhất
                ArrayList<AoDTO> currentList = tempBus.docDSA();

                if (currentList != null && !currentList.isEmpty()) {
                    for (AoDTO ao : currentList) { // Lặp qua AoDTO
                        Row row = sheet.createRow(rowNum++);
                        row.createCell(0).setCellValue(ao.getMaLoai()); // getMaLoai
                        row.getCell(0).setCellStyle(numberStyle);

                        row.createCell(1).setCellValue(ao.getTenAo()); // getTenAo
                        row.getCell(1).setCellStyle(dataStyle);
                    }
                }

                for (int i = 0; i < headers.length; i++) {
                    sheet.autoSizeColumn(i);
                }

                try (FileOutputStream outputStream = new FileOutputStream(filePath)) {
                    workbook.write(outputStream);
                    JOptionPane.showMessageDialog(this, "Xuất file Excel thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }

            } catch (IOException e) {
                JOptionPane.showMessageDialog(this, "Lỗi khi xuất file Excel: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnGhiActionPerformed

    
    
    private void btnDocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDocActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Chọn File Excel Áo");
        fileChooser.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter("Excel files (*.xlsx)", "xlsx"));
        fileChooser.setAcceptAllFileFilterUsed(false);
        int result = fileChooser.showOpenDialog(this);

        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            ArrayList<AoDTO> excelData = readExcelFile(selectedFile); // Gọi hàm đọc đúng

            if (excelData != null && !excelData.isEmpty()) {
                AoBUS bus = new AoBUS(); // Sửa BUS
                bus.docDSA(); // Đảm bảo cache BUS được load

                int successCount = 0;
                int errorCount = 0;
                int updateCount = 0;
                StringBuilder errorDetails = new StringBuilder("Các lỗi xảy ra:\n");

                for (AoDTO aoFromExcel : excelData) {
                    try {
                        if (aoFromExcel.getTenAo() == null || aoFromExcel.getTenAo().trim().isEmpty()) {
                            throw new IllegalArgumentException("Tên áo không được để trống (Mã loại Excel: " + aoFromExcel.getMaLoai() + ")");
                        }

                        // Logic: Nếu Mã Loại > 0 và tồn tại -> Cập nhật. Nếu không -> Thêm mới.
                        AoDTO existingAo = null;
                        if (aoFromExcel.getMaLoai() > 0) {
                           existingAo = bus.layAoTheoMa(aoFromExcel.getMaLoai());
                        }

                        if (existingAo != null) {
                            // Mã loại tồn tại -> Cập nhật
                            existingAo.setTenAo(aoFromExcel.getTenAo()); // Chỉ cập nhật tên
                            bus.sua(existingAo);
                            updateCount++;
                        } else {
                            // Mã loại không tồn tại hoặc không có trong file -> Thêm mới
                            // Cần đảm bảo mã loại này tồn tại trong bảng loaisp trước khi thêm áo
                             if (new LoaiSanPhamBUS().kiemTraMaLoai(aoFromExcel.getMaLoai())) {
                                AoDTO newAo = new AoDTO(aoFromExcel.getMaLoai(), "", aoFromExcel.getTenAo()); // Mã loại từ excel, tên loại rỗng, tên áo từ excel
                                bus.them(newAo); // BUS thêm sẽ tự lấy mã loại auto-increment nếu cần (kiểm tra lại logic BUS.them)
                                successCount++;
                             } else {
                                 throw new IllegalArgumentException("Mã loại " + aoFromExcel.getMaLoai() + " không tồn tại trong bảng Loại Sản Phẩm.");
                             }
                        }
                    } catch (IllegalArgumentException | NullPointerException ex) {
                        errorCount++;
                        errorDetails.append("- Dòng ứng với Mã loại Excel ").append(aoFromExcel.getMaLoai()).append(": ").append(ex.getMessage()).append("\n");
                    } catch (Exception e) {
                        errorCount++;
                        errorDetails.append("- Lỗi không xác định với Mã loại Excel ").append(aoFromExcel.getMaLoai()).append(": ").append(e.getMessage()).append("\n");
                        e.printStackTrace();
                    }
                }

                // Cập nhật lại cache trong BUS
                bus.docDSA();

                // Làm mới danh sách hiển thị trên GUI
                if (gui != null) {
                    gui.loadDataTable(bus.docDSA()); // Gọi lại hàm load của AoGUI
                }

                // Hiển thị thông báo tổng kết
                StringBuilder message = new StringBuilder("Đọc file Excel hoàn tất!\n");
                message.append("- Tổng số dòng dữ liệu đọc được: ").append(excelData.size()).append("\n");
                message.append("- Số áo thêm mới thành công: ").append(successCount).append("\n");
                message.append("- Số áo cập nhật thành công: ").append(updateCount).append("\n");
                message.append("- Số dòng bị lỗi hoặc bỏ qua: ").append(errorCount);

                JLabel lb = new JLabel("<html>" + message.toString().replace("\n", "<br>") + "</html>");
                lb.setFont(new Font("Segoe UI", Font.PLAIN, 14));
                JOptionPane.showMessageDialog(this, lb, "Thông báo", JOptionPane.INFORMATION_MESSAGE);

                if (errorCount > 0) {
                    JTextArea textArea = new JTextArea(errorDetails.toString());
                    textArea.setEditable(false);
                    JScrollPane scrollPane = new JScrollPane(textArea);
                    scrollPane.setPreferredSize(new Dimension(400, 200));
                    JOptionPane.showMessageDialog(this, scrollPane, "Chi tiết lỗi", JOptionPane.WARNING_MESSAGE);
                }

            } else if (excelData != null) {
                JOptionPane.showMessageDialog(this, "File Excel không có dữ liệu hợp lệ!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDocActionPerformed
    private ArrayList<AoDTO> readExcelFile(File file) {
        ArrayList<AoDTO> dataList = new ArrayList<>();
        try (FileInputStream fis = new FileInputStream(file);
             Workbook workbook = new XSSFWorkbook(fis)) {

            Sheet sheet = workbook.getSheetAt(0);
            Iterator<Row> rowIterator = sheet.iterator();

            if (rowIterator.hasNext()) {
                rowIterator.next(); // Bỏ qua hàng tiêu đề
            }

            while (rowIterator.hasNext()) {
                Row row = rowIterator.next();
                AoDTO ao = new AoDTO();
                boolean rowHasData = false;

                try {
                    // Đọc Mã loại (Cột 0) - Số, bắt buộc phải có để liên kết
                    Cell cellMaLoai = row.getCell(0, Row.MissingCellPolicy.RETURN_BLANK_AS_NULL);
                    if (cellMaLoai != null && cellMaLoai.getCellType() == CellType.NUMERIC) {
                        ao.setMaLoai((int) cellMaLoai.getNumericCellValue());
                        // Không set rowHasData vì Tên áo mới là thông tin chính
                    } else {
                        System.err.println("Bỏ qua dòng " + (row.getRowNum() + 1) + ": Mã loại không hợp lệ hoặc bị trống.");
                        continue; // Bỏ qua dòng này nếu Mã loại không hợp lệ
                    }

                    // Đọc Tên áo (Cột 1) - Chuỗi, bắt buộc
                    Cell cellTenAo = row.getCell(1, Row.MissingCellPolicy.RETURN_BLANK_AS_NULL);
                    if (cellTenAo != null && cellTenAo.getCellType() == CellType.STRING && !cellTenAo.getStringCellValue().trim().isEmpty()) {
                        ao.setTenAo(cellTenAo.getStringCellValue().trim());
                        rowHasData = true; // Dòng có dữ liệu hợp lệ nếu có Tên áo
                    } else {
                        System.err.println("Bỏ qua dòng " + (row.getRowNum() + 1) + ": Tên áo không hợp lệ hoặc bị trống.");
                        continue; // Bỏ qua dòng này nếu Tên áo trống
                    }

                    if (rowHasData) {
                        dataList.add(ao);
                    }

                } catch (Exception e) {
                    System.err.println("Lỗi khi đọc dòng " + (row.getRowNum() + 1) + ": " + e.getMessage());
                    continue;
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi đọc file Excel: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
            return null;
        }
        return dataList;
    }

