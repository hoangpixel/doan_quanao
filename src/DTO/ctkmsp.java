public class ctkmsp {
    public int mactkm,mactkmsp,masp,ptgg;
    public ctkmsp(){}
    public ctkmsp(int mactkm,int mactkmsp,int masp,int ptgg)
    {
        this.mactkm=mactkm;
        this.mactkmsp=mactkmsp;
        this.masp=masp;
        this.ptgg=ptgg;
    }

    public int getMactkm() {
        return mactkm;
    }

    public int getMactkmsp() {
        return mactkmsp;
    }

    public int getMasp() {
        return masp;
    }

    public int getPtgg() {
        return ptgg;
    }

    public void setMactkm(int mactkm) {
        this.mactkm = mactkm;
    }

    public void setMactkmsp(int mactkmsp) {
        this.mactkmsp = mactkmsp;
    }

    public void setMasp(int masp) {
        this.masp = masp;
    }

    public void setPtgg(int ptgg) {
        this.ptgg = ptgg;
    }
    
    
    
    
    
    
    
}
