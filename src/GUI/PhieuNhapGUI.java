    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
       int i = tblPN.getSelectedRow();
        if(i<0)
        {
           JLabel lbchonMaXoa = new JLabel("Vui lòng chọn mã để xóa");
           lbchonMaXoa.setFont(new Font("Segoe UI",Font.BOLD,16));
           JOptionPane.showMessageDialog(this, lbchonMaXoa,"Chọn mã cần xóa",JOptionPane.ERROR_MESSAGE);
           return;
        }
        
        JFrame topFrame = (JFrame) SwingUtilities.getWindowAncestor(this);
        XoaPN dialog = new XoaPN(topFrame,true);
        dialog.setVisible(true);
        if(dialog.xacNhanXoa())
        {
            int ma = (int) tblPN.getValueAt(i, 0);
            PhieuNhapBUS pnbus = new PhieuNhapBUS();
            pnbus.xoa(ma);
            model.removeRow(i);
            JLabel lb = new JLabel("Xóa thành công!");
            lb.setFont(new Font("Segoe UI", Font.BOLD, 16));
            JOptionPane.showMessageDialog(this, lb, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        int i = tblPN.getSelectedRow();
        if(i<0)
        {
           JLabel lbchonMaXoa = new JLabel("Vui lòng chọn mã để cập nhật");
           lbchonMaXoa.setFont(new Font("Segoe UI",Font.BOLD,16));
           JOptionPane.showMessageDialog(this, lbchonMaXoa,"Chọn mã cần xóa",JOptionPane.ERROR_MESSAGE);
           return;            
        }
        int ma = (int) tblPN.getValueAt(i, 0);
        int manv = (int) tblPN.getValueAt(i, 1);
        int mancc = (int) tblPN.getValueAt(i, 2);
        int tongtien = (int) tblPN.getValueAt(i,3);
        String ngayNhap = tblPN.getValueAt(i, 4).toString();
        
        PhieuNhapDTO pn = new PhieuNhapDTO(ma, manv, mancc, tongtien, ngayNhap);
        JFrame topFrame = (JFrame) SwingUtilities.getWindowAncestor(this);
        SuaPhieuNhap dialog = new SuaPhieuNhap(topFrame, true, pn);
        dialog.setVisible(true);
        
        if(dialog.xacNhanSua())
        {
            PhieuNhapBUS pnbus = new PhieuNhapBUS();
            PhieuNhapDTO phieunhap = dialog.getPN();
            pnbus.sua(phieunhap);
            model.setValueAt(phieunhap.getMaNV(),i,1);
            model.setValueAt(phieunhap.getMaNCC(),i,2);
            model.setValueAt(phieunhap.getTongTien(),i,3);
            model.setValueAt(phieunhap.getNgayNhap(),i,4);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

