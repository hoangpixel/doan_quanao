    public void headerTable(){
        Vector header = new Vector();
        header.add("Mã PN");
        header.add("Mã NV");
        header.add("Mã NCC");
        header.add("Tổng tiền");
        header.add("Ngày nhập");
        model = new DefaultTableModel(header,0)
                {
                    @Override
                    public boolean isCellEditable(int row,int column)
                {
                    return false;
                }
                };
        tblPN.setModel(model);
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(JLabel.CENTER);

        // Căn nội dung ra chính giữa
        for (int i = 0; i < tblPN.getColumnCount(); i++) {
        tblPN.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
        }

        //căn header ra center
        JTableHeader headerTB = tblPN.getTableHeader();
        DefaultTableCellRenderer center = (DefaultTableCellRenderer) headerTB.getDefaultRenderer();
        center.setHorizontalAlignment(JLabel.CENTER);
        headerTB.setFont(new Font("Segoe UI",Font.BOLD,14));
    }
    public void docDB(){
        model.setRowCount(0);
        PhieuNhapBUS pnbus = new PhieuNhapBUS();
        if(PhieuNhapBUS.ds == null){
            pnbus.docDSPN();
        }
        for( PhieuNhapDTO pn : PhieuNhapBUS.ds){
            Vector row = new Vector();
            row.add(pn.getMaPN());
            row.add(pn.getMaNV());
            row.add(pn.getMaNCC());
            row.add(pn.getTongTien());
            row.add(pn.getNgayNhap());
            model.addRow(row);
        }
        tblPN.setModel(model);
    }
